/********************************* 
        Loop and Iterations
**********************************/

// FOR Loop

for(var i =0; i<10; i++){
console.log(i);
}

// Printing array values using For Loop

var nameArray = ['a','b','c','d'];
for(var i =0; i<nameArray.length; i++){
    console.log(nameArray[i]);
}
// Printing array values using WHILE Loop
var j=0;
while(j<nameArray.length){
    console.log(nameArray[j]);
    j++;
}
